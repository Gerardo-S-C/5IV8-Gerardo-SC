    private void descifradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_descifradoActionPerformed
        // TODO add your handling code here:
        File texto = new File (descifrar.getText());        
        String password = clave.getText();
        
        if(password.length()==8){
        
        try {
            FileReader fr= new FileReader (texto);
            BufferedReader br = new BufferedReader(fr);
            
            System.out.println(br);      
            
            try{
                SecretKeyFactory skf = SecretKeyFactory.getInstance("DES");
                DESKeySpec kspec = new DESKeySpec(password.getBytes());
                SecretKey ks = skf.generateSecret(kspec);
                
                System.out.println("Clave : " + ks);
                
                Cipher cifrado = Cipher.getInstance("DES/ECB/PKCS5Padding");
                cifrado.init(Cipher.DECRYPT_MODE, ks);
                
                byte[] buffer = new byte[1000]; //quiero ir leyendo de 1000 en 1000 bits del fichero
        
                byte[] bufferCifrado; //aqui voy almacenar el resultado

                FileInputStream in = new FileInputStream(texto);

                FileOutputStream out = new FileOutputStream(texto+".descifrado");

                int bytesleidos = in.read(buffer, 0, 1000);
                while(bytesleidos != -1){
                    //mientras no se llegue al final del archivo o fichero
                    bufferCifrado = cifrado.update(buffer, 0, bytesleidos);
                    //para el texto claro leer y enviarlo al buffer cifrado
                    out.write(bufferCifrado);
                    //escribir el archivo cifrado
                    bytesleidos = in.read(buffer, 0, 1000);
                }
                bufferCifrado = cifrado.doFinal();
                out.write(bufferCifrado); //escribir el final del texto cifrado si lo hay
                
                //muestra el mensaje descifrado sin necesidad de abrir el archivo
                //manualmente linea por linea
                String texto_descifrado;
                FileReader f = new FileReader(texto+".descifrado");
                BufferedReader b = new BufferedReader(f);
                while((texto_descifrado = b.readLine())!=null) {
                    JOptionPane.showMessageDialog(null, texto_descifrado);
                }
                b.close();
                
                in.close();
                out.close();
                
                
                
            } catch (InvalidKeyException ex) {
                Logger.getLogger(Cifrado.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(Cifrado.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InvalidKeySpecException ex) {
                Logger.getLogger(Cifrado.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchPaddingException ex) {
                Logger.getLogger(Cifrado.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Cifrado.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalBlockSizeException ex) {
                Logger.getLogger(Cifrado.class.getName()).log(Level.SEVERE, null, ex);
            } catch (BadPaddingException ex) {
                Logger.getLogger(Cifrado.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null,"La clave no coincide");
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Cifrado.class.getName()).log(Level.SEVERE, null, ex);
        }

        }
        else{
            System.out.println("La clave debe de ser de 8");
        }
    }//GEN-LAST:event_descifradoActionPerformed

